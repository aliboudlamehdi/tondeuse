package tondeuse;

public class Surface {
	Integer posYMax;
	Integer posYMin=0;
	Integer posXMax;
	Integer posXMin=0;
	
	
	public Integer getPosYMax() {
		return posYMax;
	}
	public void setPosYMax(Integer posYMax) {
		this.posYMax = posYMax;
	}
	public Integer getPosYMin() {
		return posYMin;
	}
	public void setPosYMin(Integer posYMin) {
		this.posYMin = posYMin;
	}
	public Integer getPosXMax() {
		return posXMax;
	}
	public void setPosXMax(Integer posXMax) {
		this.posXMax = posXMax;
	}
	public Integer getPosXMin() {
		return posXMin;
	}
	public void setPosXMin(Integer posXMin) {
		this.posXMin = posXMin;
	}

	
}
